void solve(){

    int n; cin >> n;
    vector<int> a(n);
    for(int i = 0; i < n; i++) cin >> a[i];

    sort(a.begin(), a.end());

    int ans = 0;
    for(int i=0;i<2*n;i++){
        int x = i;
        int it = lower_bound(a.begin(), a.end(), x) - a.begin();
        int y = a.size() - it;
        // cout << x << " " << y << endl;
        if(y >= x){
            ans = x;
        }
    }
    cout << ans << endl;

}


